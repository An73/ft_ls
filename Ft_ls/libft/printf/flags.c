void	flags(char *p, char *flg)
{
	if (*p == '-')
		(*flg) = (*flg) | (1 << 0);
	if (*p == '+')
		(*flg) = (*flg) | (1 << 1);
	if (*p == ' ')
		(*flg) = (*flg) | (1 << 2);
	if (*p == '#')
		(*flg) = (*flg) | (1 << 3);
	if (*p == '0')
		(*flg) = (*flg) | (1 << 4);
}
